                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/primer.png" alt="">
                    <h2>Molienda
                        <br>
                        <small>Primer Paso</small>
                    </h2>
                    <p>Los materiales(sílece, caolín, feldespato, barro) se extraen de minas de laregión, se ponen a secar y posteriormente se mezclan en un molino de martillos, el cual los pulveriza, transformando en polvo fino.</p>
                    <hr>
                </div>
                </div>
                <div class="box">
                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/segundo.png" alt="">
                    <h2>Preparación de Pastas
                        <br>
                        <small>Segundo Paso</small>
                    </h2>
                    <p>Se mezcla la pasta en polvo con agua y posteriormente se coloca en tinas de yeso para que seque y se pueda tornear o se mantiene en estado líquido (barbotina) para el proceso de vaciado.</p>
                    <hr>
                </div>
                </div>
                <div class="box">
                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/tercer.png" alt="">
                    <h2>Torneado
                        <br>
                        <small>Tercer Paso</small>
                    </h2>
                    <p>Se amasa la pasta con la técnica de caracol para eliminar burbujas de aire, posteriormente pasa al torno en el cual se elaboran piezas redondas, desde miniaturas hasta piezas grandes como florelos o tibores, las cuales se dejan secar hasta adquirir la dureza de cuero para posteriormente darles el acabado al final en el retorneado, en donde se elimina el exceso de pasta y se rectifican bordes, se pegan asas, perillas, etc.</p>
                    <hr>
                </div>
                </div>
                <div class="box">
                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/cuarto.png" alt="">
                    <h2>Vaciado y Rectificado
                        <br>
                        <small>Cuarto Paso</small>
                    </h2>
                    <p>Se vacía la barbotina dentro del molde de yeso, donde comenzará a secarse y formar la pieza, una vez que asquiera el grosor adecuado (4 a 5mm) se vacía la barbotina restante y se deja secar la pieza hasta alcanzar la dureza de cuero para poder abrir el molde y sacar la pieza formada.</p>
                    <p>Una vez que las piezas están secas, pasan al rectificado, en el cual se eliminan bordes y rebabas para dar un mejor acabado a la pieza.</p>
                    <hr>
                </div>
                </div>
                <div class="box">
                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/quinto.png" alt="">
                    <h2>Pulido, Esmaltado y Marcado
                        <br>
                        <small>Quinto Paso</small>
                    </h2>
                    <p>Una vez libre de bordes, la pieza se pule con agua y esponja para darle un acabado terso y eliminar polvo, después se aplica una capa de esmalte en el interior para hacerla impermeable y que pueda contener liquidos.</p>
                    <p>El marcado consiste en pintar líneas de color con pincel a la pieza en boca, asas, perillas y posteriormente trazar una cuadricula con lápiz a la pieza para que esta sirva de guía al perfilado. </p>
                    <hr>
                </div>
                </div>
                <div class="box">
                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/sexto.png" alt="">
                    <h2>Perfilado y Relleno
                        <br>
                        <small>Sexto Paso</small>
                    </h2>
                    <p>El perfilado consiste en vestir a la pieza con el decorado, este se hace en color azul cobalto o café y se utiliza una perilla, la cual funciona como un lapicero, soltando gradualmente el color. Cada pieza es única, pues es la perfiladora la que decide como decorar la pieza, teniendo como base solo una cuadricula. </p>
                    <p>Una vez que la pieza tiene el decorado, pasa al relleno, que consiste el llenar de color la pieza, las decoraciones combinan los diferentes colores, aplicando flores, puntos y llenando los decorados. </p>
                    <hr>
                </div>
                </div>
                <div class="box">
                <div class="col-lg-12 text-center">
                    <img class="img-responsive img-border img-full" src="<?php echo base_url();?>img/proceso/septimo.png" alt="">
                    <h2>Horneado
                        <br>
                        <small>Séptimo Paso</small>
                    </h2>
                    <p>Una vez terminado el decorado, la pieza pasa al horno, en el cual permanecerá por un periodo de seis horas, a una temperatura de 1260°C. Durante este tiempo, la pieza, que antes de hornearse es frágil, adquiere resistencia y los esmaltes se vitrifican, adquiriendo un brillo característico.</p>
                    <hr>
